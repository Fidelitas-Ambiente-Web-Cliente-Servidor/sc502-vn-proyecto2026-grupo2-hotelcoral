-- =========================================================
-- TABLA: COR_TIPOS_HABITACION_TB
-- =========================================================
CREATE TABLE COR_TIPOS_HABITACION_TB (
    ID_TIPO_HABITACION BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    ID_HOTEL BIGINT UNSIGNED NOT NULL,
    NOMBRE_TIPO VARCHAR(100) NOT NULL,
    DESCRIPCION TEXT NULL,
    CAPACIDAD INT UNSIGNED NOT NULL,
    CANTIDAD_CAMAS INT UNSIGNED NOT NULL,
    PRECIO_BASE DECIMAL(10,2) NOT NULL,
    ESTADO_TIPO_HABITACION VARCHAR(20) NOT NULL DEFAULT 'ACTIVO',

    CONSTRAINT UQ_COR_TIPOS_HABITACION_HOTEL_NOMBRE
        UNIQUE (ID_HOTEL, NOMBRE_TIPO),

    CONSTRAINT UQ_COR_TIPOS_HABITACION_HOTEL_TIPO
        UNIQUE (ID_HOTEL, ID_TIPO_HABITACION),

    CONSTRAINT CHK_COR_TIPOS_HABITACION_CAPACIDAD
        CHECK (CAPACIDAD > 0),

    CONSTRAINT CHK_COR_TIPOS_HABITACION_CAMAS
        CHECK (CANTIDAD_CAMAS > 0),

    CONSTRAINT CHK_COR_TIPOS_HABITACION_PRECIO
        CHECK (PRECIO_BASE >= 0),

    CONSTRAINT CHK_COR_TIPOS_HABITACION_ESTADO
        CHECK (ESTADO_TIPO_HABITACION IN ('ACTIVO', 'INACTIVO')),

    CONSTRAINT FK_COR_TIPOS_HABITACION_HOTEL
        FOREIGN KEY (ID_HOTEL) REFERENCES COR_HOTELES_TB(ID_HOTEL)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IDX_COR_TIPOS_HABITACION_HOTEL
    ON COR_TIPOS_HABITACION_TB (ID_HOTEL);

CREATE INDEX IDX_COR_TIPOS_HABITACION_ESTADO
    ON COR_TIPOS_HABITACION_TB (ESTADO_TIPO_HABITACION);

