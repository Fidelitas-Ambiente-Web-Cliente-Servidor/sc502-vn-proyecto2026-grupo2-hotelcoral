-- =========================================================
-- TABLA: COR_USUARIOS_TB
-- =========================================================
CREATE TABLE COR_USUARIOS_TB (
    ID_USUARIO BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    APELLIDO VARCHAR(100) NOT NULL,
    CORREO VARCHAR(150) NOT NULL,
    CONTRASENA VARCHAR(255) NOT NULL,
    TELEFONO VARCHAR(20) NULL,
    FECHA_REGISTRO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ESTADO_USUARIO VARCHAR(20) NOT NULL DEFAULT 'ACTIVO',
    ROL VARCHAR(20) NOT NULL DEFAULT 'CLIENTE',

    CONSTRAINT UQ_COR_USUARIOS_CORREO UNIQUE (CORREO),
    CONSTRAINT CHK_COR_USUARIOS_ESTADO
        CHECK (ESTADO_USUARIO IN ('ACTIVO', 'INACTIVO', 'BLOQUEADO')),
    CONSTRAINT CHK_COR_USUARIOS_ROL
        CHECK (ROL IN ('CLIENTE', 'ADMIN'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IDX_COR_USUARIOS_APELLIDO_NOMBRE
    ON COR_USUARIOS_TB (APELLIDO, NOMBRE);

CREATE INDEX IDX_COR_USUARIOS_ROL
    ON COR_USUARIOS_TB (ROL);

-- =========================================================
-- TABLA: COR_HOTELES_TB
-- =========================================================
CREATE TABLE COR_HOTELES_TB (
    ID_HOTEL BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    NOMBRE_HOTEL VARCHAR(150) NOT NULL,
    DIRECCION VARCHAR(255) NOT NULL,
    CIUDAD VARCHAR(100) NOT NULL,
